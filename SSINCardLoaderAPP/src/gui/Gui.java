    private void send_BTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_BTActionPerformed
        
        if(card_pin_TF.getText().equals("") ||
                enc_key_TF.getText().equals("") ||
                sig_key_TF.getText().equals("") ||
                rsaKey_TF.getText().equals("") ||
                keystore_TF.getText().equals("")){
            
            JOptionPane.showMessageDialog(this,
                "Fill all the boxes.\n",
                "Warning",
                JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        
        
        cardAPI.setCardReader(cardSelection_CB.getSelectedIndex());
        cardAPI.sendToCard(card_pin_TF.getText(),
                enc_key_TF.getText(),
                sig_key_TF.getText(), 
                rsaKey_TF.getText(),
                keystore_TF.getText()
                , console_TA);
        
    }//GEN-LAST:event_send_BTActionPerformed

