    private void openFileChoose(JTextField tf, final String extension, final String extensionDescription){
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File("."));
        chooser.setAcceptAllFileFilterUsed(false);

        FileFilter filter = new FileFilter() {

            @Override
            public boolean accept(File pathname) {
                return pathname.getName().toLowerCase().endsWith(extension) || pathname.isDirectory();
            }

            @Override
            public String getDescription() {
                return extensionDescription;
            }
        };

        chooser.setFileFilter(filter);

        int r = chooser.showOpenDialog(this);
        if (r == JFileChooser.APPROVE_OPTION) {
            tf.setText( chooser.getSelectedFile().getPath());
        }
    }
    
    
    
    

